 <h3>Farmer's Right to Register Traditional Varieties:-</h3>
The PPVFR Act allows the registration of traditional varieties or farmer's varieties
. Registration of the variety grants PBR on the variety,
which allows exclusive legal right to the PBR-holding farmers to produce and market
its seed . Farmers are awarded PBR by the Act on their
recognition as breeders. Traditional varieties developed or conserved by a community
of farmers and new varieties developed by one or more farmers are eligible for
registration. In the case of registration of a traditional variety, it is important to
involve all communities associated with its conservation, if there is no clear evidence
to establish an exclusive role for the origin of the said variety. Similarly, when avariety developed by a farmer is registered, it is
important to recognize spousal contribution
under joint ownership . Farmers need
not have to pay any fee either to register their
varieties or to renew these registrations. This is a major
incentive to the innovative breeders among
farmers. On the contrary, professional breeders
and public research institutions are required to
pay separately Rs.5,000 to 10,000 as registration
and renewal fees. The PPVFR Act is unique in
respect of this right, because the plant variety
protection laws of many other countries do allow
only protection of new varieties bred by
professional breeders. The requirements of a
farmers’ variety to qualify for registration are
explained under Eligibility criteria for registration.

<h3>Farmer's Right for Reward and
Recognition:-</h3>
In recognition of the important role farmers have
been playing for the conservation of varietal
wealth of crop plants, the PPVFR Act has a
provision to reward and recognize individual
farmers or farming and tribal communities for
such contribution . According to the Act, a National Gene
Fund is to be created to facilitate reward and
recognition to eligible individual farmers and
communities . This is provided as an
incentive to encourage conservation undertaken
by farming and tribal communities. Farmers conserving traditional varieties and
wild species of crop plants are deemed eligible to receive reward and recognition.
<h3>Farmer's Right for Benefit Sharing:-</h3>
Earlier sections have highlighted the important role of farmers’ varieties for breeding
new plant varieties. Usually one or more farmer's varieties or their derivatives are
used as parents to breed new varieties. These farmers’ varieties could be traced to
have been contributed by one or more farming or tribal communities. It is also
possible that the new variety that is registered under the Act has become popular
among farmers. But because it is registered, its PBR-holder alone is entitled to
commercially produce and market its seed. The popularity and exclusive marketing
right may lead to huge seed sale and profits to the PBR-holder. The Act provides for
equitable sharing of the benefit earned from the new variety with farming or tribal
communities that had contributed varieties used as parents.Normally, all applicants who seek to register new varieties are required to declare the
source of the varieties used as parents for breeding new varieties . Farmers are given
opportunity to submit claims for benefit
share, when their variety is used as parents. Claims
for benefit share is made when breeder
discloses the use of specific varieties
conserved by farmers of a region or on the
basis of strong grounds which may
substantiate undisclosed use of specific
farmers’ variety by the breeder. Claims for
benefit share has to be made in Form PV-7 prescribed by the
Authority within six months from the date
of advertisement inviting such claims by
the Authority. On examination of such
claims by the Authority applicant eligible
for benefit sharing and the quantum of
benefit to be shared are decided. The PBRholder
of the variety is required to remit
the awarded benefit share in the National
Gene Fund . The benefit share may be disbursed
from the National Gene Fund to the eligible individual, community or institution.
Therefore for claiming eligible benefit share, the farming communities should have
(i) timely information on the varieties being registered under the Act. (ii) timely
understanding on the notification on such varieties inviting claims for benefit share,
and (iii) capability to understand the disclosed characteristics of the new variety visa-vis
those of parental varieties. The capability of farmers in accessing this right can
be strengthened by vigilant farmers’ associations and panchayats by regularly following
the notifications of the PPVFR Authority. Assistance from governmental or nongovernmental
organizations may also help farmers in this regard.

<h3>Farmer's Right to get Compensation for the Loss suffered from
the Registered Variety:-</h3>
The primary purpose of registration of a plant variety under this Act is to establish
exclusive commercial right on the variety. Commercial demand arises from the
capability of the variety for better agronomic performance. Some of the seed
companies, however, promote their seed by making exaggerated claims on the
agronomic performance of their varieties. Farmers purchasing and cultivating such
seeds may eventually realize the deceit. The PPVFR Act has a provision to check such
unfair marketing practices by breeders and their seed sellers. The Act requires that the
seed be sold with a declaration on its agronomic performance and the cultivation
conditions ensuring this performance. The performance could be higher yield, better pest or disease resistance, better quality, and so on. In the event farmers are not able
to achieve the claimed performance on having cultivated under the specified conditions,
the PBR-holder will be made liable to pay compensation to affected farmers .
Such claims for compensation are made to the PPVFR Authority in Form PV-25. The Authority on confirmation of the compensation claim
decides the amount of compensation to be paid by the PBR-holder. For establishing
the compensation claim farmers may be required to prove that they had cultivated a
specific variety with seed marketed by a specific PBR-holder, that they cultivated
variety in accordance with the recommended practices in recommended region and
that they suffered an estimated loss due to poor performance of the crop.
Compensation claims made by a group of farmers belonging to one region or an
association of farmers are likely to be more forceful than claims made by few scattered
individual farmers. The PPVFR Rules are not very explicit to be helpful to farmers in
this respect. Intensive capacity building to farmers is important for accessing legitimate
compensation and minimising the frivolous claims.

<h3>Farmer's Right to receive Compensation for Undisclosed use of ndisclosed use of
Traditional Varieties:-</h3>
It was earlier stated that the cause of benefit sharing arises from a declaration made
by the breeder that the pedigree of a new variety has certain traditional varieties or
traditional knowledge sourced from certain regions/communities. However, there
can be situations where the breeder of a new variety may not disclose the correct
identity of parental varieties or knowledge. This lapse may arise either from a honest
ignorance on the identity and origin of the parental varieties or a dishonest
suppression of parental variety identity. Under such circumstances if such parental
varieties belonged to one or more rural communities, they may be denied the
opportunity for benefit share due from the new variety. The communities concerned
also may not have the capability to detect such use of their varieties or traditional
knowledge in the breeding of a new variety. Under such situations, any third party
who has a reasonable knowledge on the possible identity of the traditional varieties
or knowledge used in the breeding of the new variety, is eligible to prefer a claim for
compensation on behalf of the concerned local or tribal community . The third party could be an NGO, an individual, a government or
private institution. Such compensation claims are to be submitted to the PPVFRAuthority
by such third party. The Authority on verification of the veracity of the
claim shall admit the same and decide on the compensation to be awarded. The
awarded compensation will be remitted in the National Gene Fund by the PBRholder.
The National Gene Fund shall disburse the compensation to the party who
made the claim. A good understanding of plant varieties and professional skill in plant
breeding science will help in detecting many of such eligible cases for compensation.

<h3>Farmer's Right for the Seeds of Registered Varieties:-</h3>
It was mentioned earlier that one of the objectives of the Act is to promote the
availability of high quality seed and planting material to farmers for accelerated agricultural development. The Act tries to achieve this objective by ensuring adequate
availability of seeds of registered varieties to farmers at reasonable cost. Access to
seed by farmers is important for availing the benefits of scientific crop improvement.
While allowing exclusive right to the PBR-holder on commercial production and
marketing of seeds, the Act directs the PBR-holder to meet farmers’ demand for
seeds of the variety at reasonable prices. According to the Act, when the PBR-holder
does not satisfy this requirement three years after registration of the variety, farmers
have the right to take the matter of non-availability of seed, its poor supply, or its
high price to the PPVFR-Authority.
On receiving such complaints and on its verification, the PVP Authority may take
remedial actions. One of these actions may be enforcement of compulsory licensing.
Compulsory licensing revokes the exclusive right on commercial production and
marketing of seed granted to the PBR-holder and transfer this right to third parties
determined by the Authority. This revoke of exclusive right is done for a period
decided by the Authority. The third parties, who are given right to produce and market
the seed, are required to meet the demands and supply seed at reasonable prices.
<h3>Farmer's Right for Receiving Free Services:-</h3>
Considering the poor economic capability of farmers and with a view that this
economic weakness shall not be a hurdle for accessing farmers’ rights, the PPVFR Act
totally exempts farmers from paying any fees . This
exemption is applicable to individual, group or community of farmers. The exemption
includes the fees required to be paid to the Registrar of Plant Varieties for registration
of farmers’ varieties, for conducting tests on them, for the renewal of registrations
and the fees prescribed for opposition, benefit claim, etc. This exemption also covers
fees on all legal proceedings at the PVPA-Tribunal or the Intellectual Property Appellate
Board (IPAB) or any Court of law. This exemption, however, does not include fee on
lawyers privately hired by farmers to represent them at the Tribunal or Appellate
Board or Courts.
<h3>Farmer's Right for Protection Against Innocent Infringement:-</h3>
All laws have penal provisions stating what constitutes an infringement and what
punishment is to be awarded to different proven infringements. These aspects of the
PPVFR Act are dealt under Chapter X of the Act . Under legal
jurisprudence, violation of a law committed out of ignorance is not held as an
admissible innocence. A safeguard to farmers against innocent infringement is
provided in the Act. According to this, a Court is
prevented from prosecution of a farmer on charges of infringement of the Act, if the
respondent farmer makes an affirmation that s/he was not aware of the legal
provision deemed to have been violated by him or her at the time of such commission.
This exceptional provision is provided in the Act in view of the low legal literacy of
tradition-bound Indian farmers and to discourage petty legal harassment to farmers
from seed companies. This highlights the importance of legal literacy to farmers on
this Act, not only to prevent infringement proceedings against them, but also to
create capability to access the rights provided to them.
